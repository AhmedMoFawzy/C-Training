#include <iostream>
#include <cmath>
using namespace std;
int main(){
        float a ,s ,d;
		 d;
		cin>>a>>s;
		d = a/s ;
	    cout <<"floor " <<a<<" / "<<s<<" = "<< floor(d) << endl;
        cout <<"ceil "<<a<<" / "<<s<<" = "<< ceil(d) << endl;
	    cout <<"round " <<a<<" / "<<s<<" = "<< round(d);
		
		return 0;
}




         